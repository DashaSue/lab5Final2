package data;

public class Coordinates {
    public double x; //Поле не может быть null
    public long y; //Максимальное значение поля: 494

    /**
     * Gets x.
     *
     * @return the x
     */
    public Double getX() {
        return x;
    }

    /**
     * Sets x.
     *
     * @param x the x
     */
    public void setX(Double x) {
        this.x = x;
    }

    /**
     * Gets y.
     *
     * @return the y
     */
    public Long getY() {
        return y;
    }

    /**
     * Sets y.
     *
     * @param y the y
     */
    public void setY(Long y) {
        this.y = y;
    }
}
